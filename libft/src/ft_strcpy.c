#include "../header/libft.h"

void	ft_strcpy(char *src, char *dest)
{
	int	i;

	i = 0;
	if (dest == 0)
		return ;
	while (dest[i])
	{
		src[i] = dest[i];
		i++;
	}
	src[i] = '\0';
}